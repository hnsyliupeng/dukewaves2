r = sqrt(1.2);

gauss(1) = -sqrt((3+2*r)/7);
gauss(2) = -sqrt((3-2*r)/7);
gauss(3) =  sqrt((3-2*r)/7);
gauss(4) =  sqrt((3+2*r)/7);

wt(1) = (1/2) - (6*r)^-1;
wt(2) = (1/2) + (6*r)^-1;
wt(3) = (1/2) + (6*r)^-1;
wt(4) = (1/2) - (6*r)^-1;